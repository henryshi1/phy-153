import math;

x1=30;
print(math.sin(x1));        # should print -0.988

y1 = math.radians(x1);      # converts x1 from degrees to radians
print(math.sin(y1));        # should print 0.5
print(math.exp(2));         # e^2 ~ 2.718^2 ~ 7.389, so should print 7.389

x1=2;
y1=3;
print(math.pow(x1,y1));     # x^y = 2^3, should print 8.0
print(math.sqrt(2));        # should print about 1.41
print(math.pi);             # should print 3.14
print(math.e);              # should print 2.718
